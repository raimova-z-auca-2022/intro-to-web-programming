# Print numbers from 1 to 10 using for loop
for i in range(1, 11):
    print(i)

# Print numbers from 1 to 10 using while loop
num = 1
while num <= 10:
    print(num)
    num += 1

# Iterate over the string "Python" and print each character
for char in "Python":
    print(char)

# Print even numbers from 2 to 10 using while loop
num = 2
while num <= 10:
    print(num)
    num += 2
